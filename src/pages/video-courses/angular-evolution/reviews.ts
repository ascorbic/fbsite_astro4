// indexType: 'even' or 'odd'
export function getUser(indexType: 'even' | 'odd') {
  if (reviews.length === 0) {
    throw new Error("Array must not be empty.");
  }

  let filteredIndices = reviews
    .map((_, index) => index)
    // .filter(index => indexType === 'even' ? index % 2 === 0 : index % 2 !== 0);

  if (filteredIndices.length === 0) {
    throw new Error(`No ${indexType} indices available in the reviews.`);
  }

  let randomIndex = filteredIndices[Math.floor(Math.random() * filteredIndices.length)];
  return reviews[randomIndex];
}




export function getRandomObjects() {
  if (reviews.length < 2) {
    throw new Error("Array must contain at least two objects.");
  }

  let firstIndex = Math.floor(Math.random() * reviews.length);
  let secondIndex;

  do {
    secondIndex = Math.floor(Math.random() * reviews.length);
  } while (secondIndex === firstIndex);

  // return [reviews[firstIndex], reviews[secondIndex]];

  return {
    first: reviews[firstIndex],
    second: reviews[secondIndex]
  }
}
